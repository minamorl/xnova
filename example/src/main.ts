import './index.css'
import { render, createElement, createTextElement, VirtualDomElement } from 'xnova';

// Function to create the header with a logo and navigation
const createHeader = (): VirtualDomElement => {
  return createElement('header', {
    class: 'flex justify-between items-center p-4 bg-gray-900 text-white'
  }, {},
    createElement('div', {
      class: 'text-lg font-bold'
    }, {}, createTextElement('Xnova')),
    createElement('nav', {}, {},
      createElement('ul', {
        class: 'flex space-x-4'
      }, {},
        createElement('li', {}, {}, createTextElement('Home')),
      )
    )
  );
};

