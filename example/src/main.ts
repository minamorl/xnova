// Function to create the footer with social media links
const createFooter = (): VirtualDomElement => {
  return createElement('footer', {
    class: 'flex justify-center items-center p-4 bg-gray-800 text-white'
  }, {},
    createElement('div', {
      class: 'flex space-x-4'
    }, {},
      createElement('a', { href: 'https://github.com/minamorl/xnova', class: 'hover:text-gray-300' }, {}, createTextElement('GitHub')),
      // Add more social links as needed
    )
  );
};

// Root component that renders the web3 welcome page
export const Web3WelcomePage = (): VirtualDomElement => {
  return createElement('div', {
    class: 'flex flex-col h-screen'
  }, {},
    createHeader(),
    createMainSection(),
    createFooter()
  );
};


// Example of how to use the renderWeb3WelcomePage function
document.addEventListener('DOMContentLoaded', () => {
  const rootElement = document.getElementById('root');
  if (rootElement) {
    const web3WelcomePageElement = Web3WelcomePage();
    // Use the render function from the library
    render(web3WelcomePageElement, rootElement);
    }
});
